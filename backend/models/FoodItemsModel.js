import mongoose from 'mongoose';

const ItemsSchema = new mongoose.Schema({
    CategoryName: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    img: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    description: {
        type: String,
        required: true
    }
});

const FoodItems = mongoose.model('food_items', ItemsSchema);

export default FoodItems;